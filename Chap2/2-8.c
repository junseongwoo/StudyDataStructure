#include <stdio.h>

int main(void)
{
	int i = 10, j = 20;
	int* ptr;

	printf("\n i의 값 : %d, j의 값 : %d", i, j);
	printf("\n i의 주소 : %d", &i);
	printf("\n j의 주소 : %d", &j);

	ptr = &i;
	printf("\n\n <<ptr = &i 실행 >>");
	printf("\n ptr의 주소(&ptr) : %u", &ptr);
	printf("\n ptr의 주소(ptr) : %u", ptr);
	printf("\n ptr의 참조 값(*ptr) : %d", *ptr);

	ptr = &j;
	printf("\n\n <<ptr = &j 실행 >>");
	printf("\n ptr의 주소(&ptr) : %u", &ptr);
	printf("\n ptr의 주소(ptr) : %u", ptr);
	printf("\n ptr의 참조 값(*ptr) : %d", *ptr);

	i = *ptr;
	printf("\n\n <<i = *ptr 실행 >>");
	printf("\n i의 값 = %d", i);

	return 0;
}